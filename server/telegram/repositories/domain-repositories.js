function createRepository(definition, db = null) {
  return new TelegramBaseRepository({
    db,
    entityName: definition.entityName,
    tableName: definition.tableName,
    idColumn: definition.idColumn,
    writableColumns: definition.writableColumns || [],
    jsonColumns: definition.jsonColumns || [],
  });
}

export function createTelegramRepositories(db, options = {}) {
  if (db && options.ensureSchema !== false) {
    ensureTelegramSchema(db);
  }

  return Object.freeze(
    Object.fromEntries(
      Object.entries(telegramRepositoryDefinitions).map(([key, definition]) => [
        key,
        createRepository(definition, db),
      ])
    )
  );
}

export const telegramRepositories = createTelegramRepositories(null, { ensureSchema: false });
export { telegramRepositoryDefinitions };
